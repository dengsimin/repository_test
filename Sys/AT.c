// Parameters:
//  None
// Return:
//  None
// Purpose:
//  Auto test command
// Notes:
//  None
//******************************************************************************
void AT_Command(UCHAR cmd)
{
static UCHAR ucAllPass = 0;
    printf("OK\n");

    switch (cmd) {
        case 0:
            UserData.ucInputSource = INPUT_SC_VGA;
//            UserSetInputSource();
            OSDConfirmInput();
            ucAllPass |= BIT0;
            break;
        case 1:
            UserData.ucInputSource = INPUT_SC_HDMI2;
//            UserSetInputSource();
            OSDConfirmInput();
            ucAllPass |= BIT1;
            break;
        case 2:
            UserData.ucInputSource = INPUT_SC_DP;
//            UserSetInputSource();
            OSDConfirmInput();
            ucAllPass |= BIT2;
            break;
        case 4:
            if (ucAllPass == (BIT2|BIT1|BIT0)) {
                printf("AllPass\n");
            }
            else {
                printf("ChkFail\n");
            }
            break;
    }

}


