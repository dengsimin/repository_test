//******************************************************************************
// Prototype: 
//  void CloseLogo(void)
// Parameters:
//  None
// Return:
//  None
// Purpose:
//  Close logo and init some user setting
// Notes:
//  None
//******************************************************************************
void CloseLogo(void)
{
    PanelPowerOffSequence();
    GMAPI_EnableGamma();    
    Custom_System.bLogoONState = OFF;    
    OSDCloseAll();
    SetPostBGColor(0, 0, 0);
    UserSetBrightness();
    UserSetContrast();
    UserSetColorTemp();
    OSDInitialize(TRUE);
}
