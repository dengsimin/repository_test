#if ENABLE_LPD == ON
/**************************************************************************//**
 * Initialize low power detection and enable it.
 ******************************************************************************/
void McuInitLPD(void)
{
    SC_HW_RST = 0x11;
#if IS_NT68750_SERIES || IS_NT68652_SERIES || IS_NT68169_SERIES || IS_NT68661_SERIES
    if (LPD_INPUT_PIN == 0) {
        SC_MCU_IO_CTRL3 &= ~(BIT7|BIT6);
    }
    else {
        SC_MCU_IO_CTRL3 &= ~(BIT5|BIT4);
    }
#elif IS_NT68360_SERIES
    SC_MCU_IO_CTRL4 |= LPD_INPUT_PIN;    
#elif IS_NT68850_SERIES || IS_NT68770_SERIES || IS_NT68655_SERIES || IS_NT68150_SERIES
    SC_MCU_IO_CTRL3 &= ~(BIT5|BIT4);
#elif IS_NT68790_SERIES_EARLIER
    SC_MCU_IO_CTRL2 |= LPD_INPUT_PIN;
#endif
    LPD0_ADJ = LPD_THRESHOLD;
#if IS_NT68790_SERIES || IS_NT68810_SERIES || IS_NT68870_SERIES
    LPD_CTRL = 0x40 | (LPD_INPUT_PIN << 1);
#else
    LPD_CTRL = 0x40;
#endif
    INTEXT_FLG = INT_LPD;
}
#endif
